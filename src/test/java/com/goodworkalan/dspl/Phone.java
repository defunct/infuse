package com.goodworkalan.dspl;

public class Phone
{
    private String type;
    
    private String number;
    
    public void setType(String type)
    {
        this.type = type;
    }
    
    public String getType()
    {
        return type;
    }
    
    public void setNumber(String number)
    {
        this.number = number;
    }
    
    public String getNumber()
    {
        return number;
    }
}
